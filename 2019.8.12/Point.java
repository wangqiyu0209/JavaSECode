package com.neuedu.test2;
/**
* @ClassName: Point
* @Description: 创建一个点的类，来表示shape的位置
* @author wqy
* @date 2019年8月10日 下午8:52:41
*
*/
public class Point {
	public int x;
	public int y;
	
	public Point() {
		super();
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
}
